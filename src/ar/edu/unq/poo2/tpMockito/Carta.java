package ar.edu.unq.poo2.tpMockito;

public class Carta {

	private String valor;
	private String palo;
	
	public Carta(String valor, String palo) {
		super();
		this.valor = valor;
		this.palo = palo;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public String getPalo() {
		return palo;
	}

	public void setPalo(String palo) {
		this.palo = palo;
	}
	
	
}
